function renderJourneysCard(
  journeys: NonNullable<MemoryModel['journeys']>,
): string {
  const items = journeys.length > 0 ? journeys : [];
  const journeyBlocks = items.map((j, i) => {
    const y = 100 + i * 72;
    const steps = j.steps.slice(0, 5).map((s) => s.name);
    const flow = steps.join(' → ');
    return `
      <text x="40" y="${y}" font-family="system-ui,sans-serif" font-size="16" fill="#1d1d1f" font-weight="600">${esc(j.signature.name)}</text>
      <text x="40" y="${y + 22}" font-family="system-ui,sans-serif" font-size="13" fill="#515154">${esc(truncate(flow, 90))}</text>
      ${i < items.length - 1 ? `<line x1="40" y1="${y + 38}" x2="760" y2="${y + 38}" stroke="#e8e8ed"/>` : ''}
    `;
  }).join('');

  return svgWrap(800, Math.max(320, 120 + items.length * 72), `
    <rect width="800" height="${Math.max(320, 120 + items.length * 72)}" rx="24" fill="#fbfbfd"/>
    <text x="40" y="50" font-family="system-ui,sans-serif" font-size="13" fill="#5e5ce6" font-weight="600" letter-spacing="1">USER JOURNEYS</text>
    <text x="40" y="82" font-family="Georgia,serif" font-size="24" fill="#1d1d1f" font-weight="600">How users move through the product</text>
    ${journeyBlocks || '<text x="40" y="140" font-family="system-ui,sans-serif" font-size="14" fill="#86868b">No journeys discovered yet</text>'}
    <text x="40" y="${Math.max(300, 100 + items.length * 72)}" font-family="system-ui,sans-serif" font-size="12" fill="#86868b">Mnemos Journey Map</text>
  `);
}


import { mkdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import type { MemoryModel } from './types.js';
import { computeMemoryScore } from './report.js';
import { buildAgentExports } from './agent-mode.js';
import { computeAiReadiness } from './ai-readiness.js';

export interface SnapshotResult {
  outputDir: string;
  files: string[];
}

export async function generateSnapshots(
  memory: MemoryModel,
  outputDir: string,
): Promise<SnapshotResult> {
  const snapDir = path.join(outputDir, 'snapshots');
  await mkdir(snapDir, { recursive: true });

  const memoryScore = computeMemoryScore(memory);
  const aiReadiness = computeAiReadiness(memory);
  const capabilities = memory.capabilities ?? [];
  const journeys = memory.journeys ?? [];
  const exports = buildAgentExports({
    memory,
    capabilities,
    journeys,
    memoryScore: memoryScore.overall,
  });

  const files: Record<string, string> = {
    'dna-summary.svg': renderDnaSummary(memory, exports.dna, memoryScore, aiReadiness),
    'capabilities.svg': renderCapabilitiesCard(capabilities.slice(0, 6)),
    'journeys.svg': renderJourneysCard(journeys.slice(0, 4)),
    'health-score.svg': renderHealthCard(memoryScore, aiReadiness),
    'architecture.svg': renderArchitectureCard(memory),
    'ai-context.svg': renderAiContextCard(memory, aiReadiness, exports.dna),
    'journey-flow.svg': renderJourneyFlowCard(journeys.slice(0, 1)),
    'terminal-build.svg': renderTerminalBuildCard(memory, memoryScore, aiReadiness),
    'benchmark-results.svg': renderBenchmarkResultsCard(memory),
  };

  const written: string[] = [];
  for (const [name, content] of Object.entries(files)) {
    const filePath = path.join(snapDir, name);
    await writeFile(filePath, content, 'utf-8');
    written.push(filePath);
  }

  return { outputDir: snapDir, files: written };
}

